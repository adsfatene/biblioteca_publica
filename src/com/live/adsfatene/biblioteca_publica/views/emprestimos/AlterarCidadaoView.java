    public void setVisible(boolean flag) {
        if (flag) {
            setLocationRelativeTo(listaView.getCidadaosController().getAplicacaoController().getAplicacaoView());
            cidadao = listaView.getCidadaosController().getAplicacaoController().getEmprestimosController().getListaView().getCadastroView().getCidadao();
            jTextFieldCidadao.setText(listaView.getCidadaosController().getAplicacaoController().getEmprestimosController().getListaView().getCadastroView().getjTextFieldCidadao().getText());
        } else {
            listaView.getCidadaosController().getAplicacaoController().getEmprestimosController().iniciar();
        }
        listaView.getCidadaosController().getAplicacaoController().getEmprestimosController().getListaView().getCadastroView().getjToggleButtonAlterarCidadao().setSelected(flag);
        listaView.getCidadaosController().getAplicacaoController().getEmprestimosController().getListaView().getCadastroView().setVisible(!flag);
        listaView.habilitar(!flag);
        super.setVisible(flag);
    }

