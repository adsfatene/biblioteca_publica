    private void jButtonConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConfirmarActionPerformed
        if (cidadao == null) {
            JOptionPane.showMessageDialog(this, "cidadao é obrigatorio. selecione um cidadao", "aviso", JOptionPane.WARNING_MESSAGE);
        } else {
            listaView.getCidadaosController().getAplicacaoController().getEmprestimosController().getListaView().getCadastroView().setCidadao(cidadao);
            listaView.getCidadaosController().getAplicacaoController().getEmprestimosController().getListaView().getCadastroView().getjTextFieldCidadao().setText(cidadao.toString());
            listaView.getCidadaosController().getAplicacaoController().getEmprestimosController().alterarCidadao(false);
        }
    }//GEN-LAST:event_jButtonConfirmarActionPerformed

    private void jButtonSelecionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSelecionarActionPerformed
        if (listaView.getjTableLista().getSelectedRowCount() < 1) {
            JOptionPane.showMessageDialog(this, "cidadao é obrigatorio. selecione um cidadao", "aviso", JOptionPane.WARNING_MESSAGE);
        } else {
            cidadao = (Cidadao) listaView.getjTableLista().getValueAt(listaView.getjTableLista().getSelectedRow(), 0);
            jTextFieldCidadao.setText(cidadao.toString());
        }
    }//GEN-LAST:event_jButtonSelecionarActionPerformed
